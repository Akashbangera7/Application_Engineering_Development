    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed

       // Patient p = new Patient();
        // TODO add your handling code here:
        try{
           // System.out.println("hosp"+enterprise);
           if(enterprise.getPatientDirectory()==null)
           {
               enterprise.setPatientDirectory(new PatientDirectory());
           }
           for(Patient p:enterprise.getPatientDirectory().getPatientList())
           {
               if(p.getPatientId().equalsIgnoreCase(txtPatientId.getText()))
               {
                   JOptionPane.showMessageDialog(null, "please add different patient id");
                   return;
           }
           }
           
           
           
        Patient  p= enterprise.getPatientDirectory().addPatient();
        SimpleDateFormat parser = new SimpleDateFormat("MM/dd/yy");
            p.setPatientId(txtPatientId.getText());
            p.setPatientName(txtPatientName.getText());
            p.setAge(Integer.parseInt(txtAge.getText()));
            p.setDateOfBirth(parser.parse(txtDateOfBirth.getText()));
            p.setIssue(txtIssue.getText());
            if(btnMale.isSelected())
        {
            p.setGender("Male");
        }
        else 
        {
            p.setGender("Female");
        }
            
             JOptionPane.showMessageDialog(null, "patient  Added Successfully");
                                
        }
       catch(Exception e){
           
           
           {
               
               JOptionPane.showMessageDialog(null, "Enter Valid Values");
           
           }
           
       }
        populateTable();
    }//GEN-LAST:event_btnCreateActionPerformed

