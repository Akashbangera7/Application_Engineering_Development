    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) tblPatientDetails.getModel();
        
        model.setRowCount(0);
        if(enterprise.getPatientDirectory()==null)
           {
               enterprise.setPatientDirectory(new PatientDirectory());
           }
        
        for(Patient h : enterprise.getPatientDirectory().getPatientList()){
        //for(Hospital h : tblPatientDetails.gethospitalList){
       // for (Hospital h : organization.getHospitalDirectory().gethospitalList()){
            
            Object[] row = new Object[6];
            row[0] = h;
            row[1] = h.getPatientName();
            row[2] = h.getAge();
            row[3] = h.getGender();
            row[4] = h.getDateOfBirth();
            row[5] = h.getIssue();
            model.addRow(row);
        }
    }
    

    
